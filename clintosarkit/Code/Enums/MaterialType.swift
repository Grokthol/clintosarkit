// a material type that can be applied to objects in the scene to change their appearance
enum MaterialType: String {
    case none = "none"
    case copper = "copper"
    case iron = "iron"
    case limestone = "limestone"
    case sandstone = "sandstone"
    case plastic = "plastic"
    case rubber = "rubber"
}
